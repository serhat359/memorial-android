package com.example.memorialandroid;

public class ListAdapterObject {
	public String textAbove;
	public String textBelow;

	public ListAdapterObject() {
		super();
	}

	public ListAdapterObject(String textAbove, String textBelow) {
		super();
		this.textAbove = textAbove;
		this.textBelow = textBelow;
	}
}
